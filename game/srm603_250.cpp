    int findend(vector <int> edges, vector <int> costs)  {
		int n = (int) costs.size();
		memset(degree, 0, sizeof(degree));

		for(int i = 0; i < n - 1; i++) {
			degree[i + 1]++;
			degree[edges[i]]++;
		}
        int ret = 0;
		for(int i = 0; i < n; i++) {
			if(degree[i] == 1) {
				ret = max(ret, costs[i]);
			}
		}
        return ret;
     }
    
